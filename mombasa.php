                            <br><br>
                            <p style="color: grey;font-size: 30px;">Mombasa county is host to the second-largest city in Kenya, Mombasa city. Famous for its white sandy beaches, palm trees, and cool breeze, county 001 has a lot to offer. In this article, we will cover affordable things to do in Mombasa for the adventure traveler.</p>
                            <br><br>
                        </div>
                        <ul>
                            <li style="font-size: 40px;">
                                <h1 style="color: black;font-size: 50px;">Take a beach walk</h1>
                                <br>
                                <img src="assets/img/walk.jpg" width="100%" height="auto" alt="">
                                <br><br>
                                <p style="color: grey;font-size: 25px;">Kenya experiences warm weather almost all year round and the coastal strip wears the sun like an expensive jewel. So, take a walk on the beaches. Public beaches are a great place to enjoy the sun and the ocean. The Jommo Kenyatta Public Beach (Pirates), Bamburi, and Nyali beaches are all accessible to the public at no cost.
                                    Weekdays are the best for beach walks because the beaches can get really full during the weekend.</p>
                                <br><br>
                            </li>
                            <li style="font-size: 40px;">
                                <h1 style="color: black;font-size: 50px;">Mombasa Cultural Centers</h1>
                                <br>
                                <img src="assets/img/culture.jpg" width="100%" height="auto" alt="">
                                <br><br>
                                <p style="color: grey;font-size: 25px;">Mombasa will not disappoint you when it comes to exploring the culture. Just 10 kilometers (6.21 miles) north of Mombasa Island, one will find the Ngomongo Villages. Ngomongo is a tribal cultural village where tourists are immersed into local culture without having to leave the city.</p>
                                <p style="color: grey;font-size: 25px;">Another great place is the Bombolulu cultural center. This non-profit organization produces crafts of a high standard and gives vocational training to physically disabled people. Visit the workshops and showroom to buy jewelry, leatherwork, sisal bags, carvings, and textiles.</p>
                                <br><br>
                            </li>
                            <li style="font-size: 40px;">
                                <h1 style="color: black;font-size: 50px;">National Parks and sanctuaries</h1>
                                <br>
                                <img src="assets/img/kws.jpg" width="100%" height="auto" alt="">
                                <br><br>
                                <p style="color: grey;font-size: 25px;">The Mombasa Marine National Park has warm azure ocean waters and swaying coconut palms on white sandy beaches. The park lies between Mtwapa and Tudor Creeks. Its blue waters are ideal for windsurfing, water skiing, snorkeling, and diving.</p>
                                <p style="color: grey;font-size: 25px;">Haller Park:  It is the transformation of a quarry wasteland into an ecological area. Haller Park holds a variety of plant and animal species. The park attractions include the game sanctuary, reptile park, fish farm, crocodile pens, giraffe viewing area, and palm gardens.</p>
                                <p style="color: grey;font-size: 25px;">Nguuni Nature Sanctuary: The sanctuary is home to a diverse range of African wildlife. Dive into nature you walk through the Acacia woodland. You will discover families of Giraffe, Ostrich, Oryx, and Elands wandering through their natural habitats.</p>
                                <p style="color: grey;font-size: 25px;">The KIbarani Recreational Park: This was once a huge dumpsite that has now been turned into a beautiful recreational ark. It is located within the Makupa Causeway (the causeway that links the mainland to the Mombasa island. It now hosts more than 500,000 plant species among them Ashok, Christmas Palm, Jacaranda, Bougainvillea, coconut, sisal, and Casuarina trees.</p>
                                <br><br>
                            </li>
                            <li style="font-size: 40px;">
                                <h1 style="color: black;font-size: 50px;">Iconic places to visit in Mombasa</h1>
                                <br>
                                <img src="assets/img/iconic.jpg" width="100%" height="auto" alt="">
                                <br><br>
                                <p style="color: grey;font-size: 25px;">Fort Jesus: Now a world heritage site, Fort Jesus is as iconic as it gets. It has been standing since 1593 by the Portuguese. The Kenyan government renovates the site and has tried to maintain its original look and feel. A trip to Mombasa is incomplete without a visit to Fort Jesus.</p>
                                <p style="color: grey;font-size: 25px;">Old Town Mombasa: If you want to see coastal architecture, then Old town Mombasa is the place to visit. You will see ancient architecture, shops, and market areas. The locals have infused their culture through the foods and souvenirs you will find along these streets. These sights and sounds provide a reflection of the Arab, Portuguese, Asian and British inhabitants who reside in Mombasa.</p>
                                <p style="color: grey;font-size: 25px;">Mamba Village: Mamba is Swahili for crocodiles and the Mamba village is the largest crocodile farm in East Africa. Prominent attractions of the village center include the botanical garden and an aquarium. The specialty here is aquatic plants and orchids. </p>
